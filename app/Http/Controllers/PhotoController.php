    {
        //
        $photo=new Photo;
        $photo->save();
        $hash=md5("$photo->id HUWebApps");
        $ext=$request->file('file')->getClientOriginalExtension();
        $filename="$hash.$ext";
        Storage::put(
            $filename,
            file_get_contents($request->file('file')->getRealPath())
          );
        $url=Storage::url($filename);
        $photo->url=$url;
        $photo->save();
        return "you've added $url";

    }

